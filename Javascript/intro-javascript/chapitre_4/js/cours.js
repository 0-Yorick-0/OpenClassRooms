console.log("Début de la boucle while");

var nombre = 1;

while(nombre <= 5){
    console.log(nombre);
    nombre++;
}
console.log("fin de la boucle");

console.log("Début de la boucle for");


for (var nombre = 1; nombre <= 5; nombre++) {
    console.log(nombre);
}
console.log("fin de la boucle for");